def rtu_crc16(data: bytes) -> int:
    """
    Calculate Modbus RTU CRC16
    
    Args:
        data: Bytes to calculate CRC for (ID through d8)
        
    Returns:
        16-bit CRC value
    """
    crc_hi = 0xFF
    crc_lo = 0xFF
    
    for byte in data:
        idx = crc_lo ^ byte
        crc_lo = crc_hi ^ TBL_CRC_HI[idx]
        crc_hi = TBL_CRC_LO[idx]
    
    return (crc_hi << 8) | crc_lo


def build_uimessage(device_id: int, cw: int, data: bytes = b'', need_ack: bool = True, use_crc: bool = True) -> bytes:
    """
    Build a UIMessage (16-byte fixed frame)
    
    Frame structure:
    - SM: 0xAA (with CRC) or 0xAD (no CRC)
    - ID: 1 byte (device ID)
    - CW: 1 byte (bit7 = ACK needed, bits 6:0 = function code)
    - DL: 1 byte (data length, 0-8)
    - d0-d7: 8 data bytes (low byte first, pad with 0x00)
    - d8: 1 auxiliary byte (don't care, set to 0x00)
    - R1:R0: 2 bytes CRC16 (if use_crc=True)
    - EM: 0xCC (fixed)
    
    Args:
        device_id: Device CAN ID
        cw: Control word (function code, 0x00-0x7F)
        data: Data bytes (max 8 bytes, low byte first)
        need_ack: Set bit7 of CW to request ACK
        use_crc: Use CRC (0xAA) or not (0xAD)
        
    Returns:
        16-byte UIMessage
    """
    # Set CW bit7 for ACK
    if need_ack:
        cw = cw | 0x80
    else:
        cw = cw & 0x7F
    
    # Pad data to 8 bytes
    data_padded = data[:8] + b'\x00' * (8 - len(data))
    dl = len(data)  # Data length
    
    # Build message without CRC and EM
    if use_crc:
        sm = 0xAA
        # Message for CRC calculation: ID through d8 (13 bytes)
        msg_for_crc = bytes([device_id, cw, dl]) + data_padded + b'\x00'  # d8 = 0x00
        crc = rtu_crc16(msg_for_crc)
        crc_lo = crc & 0xFF
        crc_hi = (crc >> 8) & 0xFF
        message = bytes([sm, device_id, cw, dl]) + data_padded + bytes([0x00, crc_lo, crc_hi, 0xCC])
    else:
        sm = 0xAD
        message = bytes([sm, device_id, cw, dl]) + data_padded + bytes([0x00, 0x00, 0x00, 0xCC])
    
    return message


def parse_uimessage(message: bytes) -> dict:
    """
    Parse a UIMessage
    
    Args:
        message: 16-byte UIMessage
        
    Returns:
        Dictionary with parsed fields
    """
    if len(message) != 16:
        raise ValueError(f"Invalid message length: {len(message)} (expected 16)")
    
    sm = message[0]
    device_id = message[1]
    cw = message[2]
    dl = message[3]
    data = message[4:12]  # d0-d7
    d8 = message[12]
    
    if sm == 0xAA:
        crc_lo = message[13]
        crc_hi = message[14]
        crc = (crc_hi << 8) | crc_lo
        use_crc = True
    else:
        crc = None
        use_crc = False
    
    em = message[15]
    
    need_ack = (cw & 0x80) != 0
    function_code = cw & 0x7F
    
    return {
        'sm': sm,
        'device_id': device_id,
        'cw': cw,
        'function_code': function_code,
        'need_ack': need_ack,
        'dl': dl,
        'data': data[:dl],  # Only return valid data bytes
        'd8': d8,
        'crc': crc,
        'use_crc': use_crc,
        'em': em
    }


